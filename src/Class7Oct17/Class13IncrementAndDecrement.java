package Class7Oct17;

public class Class13IncrementAndDecrement {

	public static void main(String[] args) {
		int num1 = 90;
		num1 = num1 + 1;

		System.out.println(num1); // 91

		num1++;
		System.out.println(num1); // 92

		num1++;
		System.out.println(num1); // 93

		int num2 = 100;
		num2 -= 1;
		System.out.println(num2);// 99
		
		num2-=1;
		System.out.println(num2); //98

		// 100 --; CE
		// increment & decrement operators can be used ONLY with variables.
		// we use these operators in our loops.
		
		
		
		System.out.println("        \n");
		
		
		
		
		
	}

}
