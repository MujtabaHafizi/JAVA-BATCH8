package Class9;

public class NestedLoopDemo2PrintingCarOdometer {

	public static void main(String[] args) {

	
			for (int a = 0; a <= 9; a++) {
				for (int ab = 0; ab <= 9; ab++) {
					for (int ac = 0; ac <= 9; ac++) {
						for (int b = 0; b <= 9; b++) {
							System.out.println(a + ""+ab+""+ac+"" + b);
						}
					}
				}
			}
		}
		
		
		
	}

