package Class8;

public class Class08ForLoops {

	public static void main(String[] args) {

		for (int i = 1; i <= 6; i++) {
			System.out.println("Springfield " + i);
			// It prints (The value of i inside the loop six times.
		}

		int result = 1;
		for (int i = 10; i >= 1; i -= 2) {
			result *= 1;
		}
		System.out.println(result); // 10,80,480,1920, 3840

	}

}
