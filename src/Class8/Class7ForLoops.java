package Class8;

public class Class7ForLoops {

	public static void main(String[] args) {
		System.out.print("it prints from 20 to 50\n");
		for (int i = 20; i <= 50; i++) {
			System.out.print(i + " ");
		}

		System.out.print("  \n");
		System.out.print("it prints from 50 to 20\n");
		for (int i = 50; i >= 20; i--) {
			System.out.print(i + " ");

		}
	}

}
